#pragma once

// =============================================================================
//  config.hpp — центральная конфигурация DLL под Stay Out.
//
//  Здесь собраны все «магические» параметры, которые раньше были бы разбросаны
//  по коду: имя окна игры, размеры игрока для ESP, FOV, система координат.
//  Правь их в одном месте.
//
//  ПРИВЯЗКА К ИГРЕ:
//    • Процесс игры        — sogame.exe (используется инжектором).
//    • Заголовок окна      — "Stay Out" (используется overlay через FindWindow).
//    • Встроенный Python   — 2.7 (python27.dll, классический BigWorld).
//    • Архитектура клиента — x64 (под неё уже написан Manual Map инжектор).
//
//  СИСТЕМА КООРДИНАТ:
//    Stay Out / BigWorld Technology — правосторонняя система. Ось «вверх»
//    зависит от сборки мира. Эмпирически для Stay Out это Y-up (как в Source):
//      X — восток, Y — вверх, Z — север.
//    Если после первого инжекта box'ы ESP рисуются «лёжа» — переключи на ZUp.
// =============================================================================

#include <string>

namespace config {

/// Ось роста игрока в мировой системе координат.
enum class CoordSystem {
    YUp,  // рост по Y (Source/Unity-подобные) — значение по умолчанию для Stay Out
    ZUp,  // рост по Z (классический BigWorld / WoT)
};

struct Config {
    // Заголовок окна игры для overlay (FindWindowW).
    // Процесс — sogame.exe, но заголовок окна типично "Stay Out".
    // Если FindWindow не найдёт окно из-за суффикса версии/региона — поправь
    // здесь или расширь overlay::initialize частичным поиском через EnumWindows.
    std::wstring window_title = L"Stay Out";

    // --- Параметры ESP ---
    float esp_max_distance = 300.f;  // радиус отрисовки в игровых единицах (метрах)
    float player_height    = 1.8f;   // условный рост человека
    float player_width     = 0.6f;   // условная ширина
    float default_fov_deg  = 75.f;   // fallback FOV, если BigWorld.camera().fov недоступен

    // Система координат движка. ZUp → ESP рисует рост по Z, иначе по Y.
    CoordSystem coord_system = CoordSystem::YUp;
};

// Глобальный экземпляр конфигурации.
// `inline` гарантирует единственное определение на всю программу (C++17+),
// никаких отдельных config.cpp не требуется.
inline Config g_config{};

} // namespace config
