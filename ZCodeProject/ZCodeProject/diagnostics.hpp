#pragma once

// =============================================================================
//  diagnostics.hpp — исследовательские дампы для RE реальных имён атрибутов
//  BigWorld-объектов под Stay Out.
//
//  Зачем это нужно:
//    В BigWorld имена атрибутов сущностей (.position, .health, .displayName ...)
//    зависят от конкретной игры и версии. Точные имена мы узнаём, вызвав
//    встроенную функцию Python dir() на живом объекте. Эти функции делают
//    именно это и возвращают результат строкой для лога.
//
//  Все функции ВСЕГДА вызываются внутри py::GilScope (захват GIL берёт на себя
//  реализация). На любом сбое возвращают строку с описанием проблемы — краша
//  игры не происходит (SEH + проверки валидности).
// =============================================================================

#include <string>

namespace diagnostics {

/// Сводка состояния моста: готовность Python, доступность модуля BigWorld,
/// наличие локального игрока. Полезна как первая проверка после инжекта.
[[nodiscard]] std::string health_check() noexcept;

/// dir(BigWorld.player()) — список атрибутов объекта локального игрока.
/// Возвращает пустую строку, если игрок не в игре (BigWorld.player() is None).
[[nodiscard]] std::string dump_player_dir() noexcept;

/// dir(BigWorld.camera()) — список атрибутов объекта камеры.
[[nodiscard]] std::string dump_camera_dir() noexcept;

/// dir(первая сущность из BigWorld.entities) — атрибуты произвольной сущности
/// в мире. Удобно, когда игрока ещё нет (например, на экране загрузки).
[[nodiscard]] std::string dump_first_entity_dir() noexcept;

} // namespace diagnostics
